# 2.	Создайте матрицу 8х8 состоящую из 1 и 0 и заполненную в шахматном порядке

import numpy as np

# Создаем матрицу 8x8 и заполняем ее в шахматном порядке
chessboard = np.zeros((8, 8), dtype=int)

# Заполняем белые клетки (1) и черные клетки (0)
chessboard[1::2, ::2] = 1  # Черные клетки в нечетных строках
chessboard[::2, 1::2] = 1  # Белые клетки в четных строках

# Выводим матрицу в консоль
print("Матрица 8x8 в шахматном порядке:")
print(chessboard)
